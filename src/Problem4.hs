problem4 :: String -> String -> String
problem4 "1" s = show findBestGuard
problem4 "2" s = show strategy2

findBestGuard :: Int
findBestGuard = id * ((snd . bestMinute) bestGuard)
    where gs = guards $ sort allGuards
          compareGuards g1 g2 = compare (totalMinutesAsleep g1) (totalMinutesAsleep g2)
          bestGuard@(Guard id _) = maximumBy (compareGuards) $ M.elems gs

strategy2 :: Int
strategy2 = (guardId guard) * ((fst . bestMinute) guard)
    where guard = mostFrequentlyAsleep allGuards

mostFrequentlyAsleep :: [Entry] -> Guard
mostFrequentlyAsleep entries = maximumBy compareSleepFrequency gs
    where gs = M.elems $ guards entries
          compareSleepFrequency g1 g2 = compare (bestMinuteValue g1) (bestMinuteValue g2)
          bestMinuteValue = snd . bestMinute


data Entry = Entry UTCTime Action
    deriving (Show, Eq)

newEntry :: String -> Action -> Entry
newEntry s a = Entry (dateTime s) a

data Action = WakeUp | FallAsleep | BeginShift Int
    deriving (Show, Eq)

data Guard = Guard Int (Map Int Int)

guardId :: Guard -> Int
guardId (Guard id _) = id

instance Ord Entry where
    compare (Entry d1 _) (Entry d2 _) = compare d1 d2

empty :: Int -> Guard
empty id = Guard id (M.fromList [ (m, 0) | m <- [0..59] ])

sleep :: Guard -> UTCTime -> UTCTime -> Guard
sleep (Guard id m) d1 d2 = Guard id newMap
    where newMap = foldl (\m minute -> M.adjust (+1) minute m) m minutesAsleep
          minutesAsleep = [e1Minutes..e2Minutes-1]
          e1Minutes = todMin $ localTimeOfDay $ utcToLocalTime utc d1
          e2Minutes = todMin $ localTimeOfDay $ utcToLocalTime utc d2

totalMinutesAsleep :: Guard -> Int
totalMinutesAsleep (Guard _ m) = foldl (+) 0 m

guards :: [Entry] -> Map Int Guard
guards entries = buildMap (sort entries) M.empty

bestMinute :: Guard -> (Int, Int)
bestMinute (Guard _ m) = (minute, count)
    where (minute, count) = maximumBy (\(_, a) (_, b) -> compare a b) $ M.assocs m


buildMap :: [Entry] -> Map Int Guard -> Map Int Guard
buildMap ((Entry _ (BeginShift id)) : rest) guards = buildMap rest $ M.adjust minutesAsleepInShift id (ensureGuardExist id)
    where ensureGuardExist k = if M.notMember k guards then M.insert k (empty id) guards else guards
          minutesAsleepInShift guard = countMinutesInShift guard rest

buildMap (_ : rest) map = buildMap rest map
buildMap [] map = map

countMinutesInShift :: Guard -> [Entry] -> Guard
countMinutesInShift guard ((Entry d1 FallAsleep) : (Entry d2 WakeUp) : rest) =
    countMinutesInShift (sleep guard d1 d2) rest
countMinutesInShift g _ = g


dateTime :: String -> UTCTime
dateTime = parseTimeOrError True defaultTimeLocale "%Y-%m-%d %H:%M"

